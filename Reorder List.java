class Solution {
    void reorderlist(Node head) {
        // Your code here
        Node slow = head;
        Node fast = head;
        while(fast != null && fast.next != null){
            slow = slow.next;
            fast = fast.next.next;
        }
        
        Node prev = null;
        Node current = slow.next;
        slow.next = null;
        while(current !=  null){
            Node temp = current.next;
            current.next = prev;
            prev = current;
            current = temp;
        }

        Node left = head;
        Node right = prev;
        while(right != null){
            Node temp1 = left.next;
            Node temp2 = right.next;
            left.next = right;
            right.next = temp1;
            left = temp1;
            right = temp2;
        }
    }
}


